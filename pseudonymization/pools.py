class PseudonymGenerator:
    """가명 생성기 - 자연스러운 이름 생성"""
    
    def __init__(self):
        self.used_fake_names = set()
        self.used_fake_provinces = set()
        self.used_fake_districts = set()
        self.phone_counter = 1
    
    def get_fake_name(self, original_name: str) -> str:
        """자연스러운 가명 이름 생성 (숫자 없이)"""
        surname_choices = ['김', '이', '박', '최', '정', '강', '조', '윤', '장', '임']
        
        # 일반적인 한국 이름들
        common_names = [
            '민준', '서준', '도윤', '예준', '시우', '하준', '주원', '지호', '준서', '준우',
            '서연', '서윤', '지우', '서현', '민서', '하윤', '윤서', '지민', '지윤', '채원',
            '지안', '시연', '예은', '수아', '서진', '예린', '다은', '하은', '소율', '예원',
            '민재', '현우', '지훈', '건우', '우진', '선우', '연우', '정우', '승현', '시현',
            '서우', '태윤', '윤우', '율', '한결', '이준', '동현', '승우', '민성', '준혁'
        ]
        
        # 랜덤으로 성씨와 이름 조합
        max_attempts = 100
        for _ in range(max_attempts):
            surname = random.choice(surname_choices)
            first_name = random.choice(common_names)
            fake_name = surname + first_name
            
            if fake_name not in self.used_fake_names:
                self.used_fake_names.add(fake_name)
                return fake_name
        
        # 만약 100번 시도해도 중복이면 숫자 추가
        surname = random.choice(surname_choices)
        first_name = random.choice(common_names)
        counter = 1
        while True:
            fake_name = f"{surname}{first_name}{counter}"
            if fake_name not in self.used_fake_names:
                self.used_fake_names.add(fake_name)
                return fake_name
            counter += 1
    
    def get_fake_age(self, original_age: str) -> str:
        """가명 나이 생성"""
        try:
            age = int(original_age)
            # 20-60 범위로 제한
            if age < 20:
                return str(random.randint(20, 30))
            elif age > 60:
                return str(random.randint(50, 60))
            else:
                # ±10년 범위
                min_age = max(20, age - 10)
                max_age = min(60, age + 10)
                return str(random.randint(min_age, max_age))
        except:
            return str(random.randint(25, 35))
    
    def get_fake_phone(self, original_phone: str) -> str:
        """가명 전화번호 생성"""
        fake_phone = f"010-0000-{self.phone_counter:04d}"
        self.phone_counter += 1
        return fake_phone
    
    def get_fake_email(self, original_email: str) -> str:
        """가명 이메일 생성"""
        domains = ['gmail.com', 'naver.com', 'daum.net', 'kakao.com']
        prefixes = ['user', 'test', 'hello', 'work', 'info', 'office', 'sample']
        
        prefix = random.choice(prefixes) + str(random.randint(1000, 9999))
        domain = random.choice(domains)
        return f"{prefix}@{domain}"
    
    def get_fake_address(self, original_address: str, address_type: str = "general") -> str:
        """가명 주소 생성"""
        if address_type == "province" or original_address in ['서울', '부산', '대구', '인천', '광주', '대전', '울산']:
            provinces = ['서울시', '부산시', '대구시', '인천시', '광주시', '대전시']
            fake_province = random.choice(provinces)
            while fake_province in self.used_fake_provinces:
                fake_province = random.choice(provinces)
            self.used_fake_provinces.add(fake_province)
            return fake_province
        
        elif address_type == "district" or original_address.endswith('구'):
            districts = ['중구', '서구', '동구', '남구', '북구', '강남구', '서초구', '송파구']
            fake_district = random.choice(districts)
            while fake_district in self.used_fake_districts:
                fake_district = random.choice(districts)
            self.used_fake_districts.add(fake_district)
            
            # 구 단위 주소라면 시와 함께 반환
            if len(original_address) <= 3:
                if '시' in original_address:
                    return original_address
                else:
                    cities = ['대전', '대구', '부산', '인천', '광주', '수원', '성남']
                    city = random.choice(cities)
                    return f"{city}시"
        
        # 기본값
        default_cities = ['서울시', '부산시', '대구시', '인천시', '광주시', '대전시']
        return random.choice(default_cities)

class DataPools:
    """최적화된 데이터풀 (CSV 내장)"""
    
    def __init__(self):
        # 탐지용 데이터
        self.real_names: Set[str] = set()
        self.real_addresses: Set[str] = set()
        self.road_names: Set[str] = set()
        self.districts: Set[str] = set()
        self.provinces: Set[str] = set()
        
        # 제외 단어 (모듈 레벨 상수 참조)
        self.name_exclude_words = NAME_EXCLUDE_WORDS
        self.compound_surnames = COMPOUND_SURNAMES
        self.single_surnames = SINGLE_SURNAMES
        
        # 가명화용 생성기
        self.generator = PseudonymGenerator()
        
        # 기타 데이터
        self.companies: List[str] = []
        
        self._initialized = False
    
    def initialize(self, custom_data: Dict = None):
        """초고속 초기화 (CSV 파일 없이)"""
        if self._initialized:
            return
        
        print("빠른 데이터풀 초기화 (CSV 내장)...")
        start_time = time.time()
        
        # 탐지용 이름 생성
        self._generate_detection_names()
        
        # 탐지용 주소 데이터 준비
        self._prepare_address_data()
        
        # 기타 데이터 생성
        self._generate_company_data()
        
        # 커스텀 데이터 적용
        if custom_data:
            self._apply_custom_data(custom_data)
        
        self._initialized = True
        
        init_time = time.time() - start_time
        print(f"빠른 데이터풀 초기화 완료! ({init_time:.3f}초)")
            
        print(f"탐지 이름: {len(self.real_names):,}개")
        print(f"탐지 도로: {len(self.road_names):,}개")
        print(f"탐지 시군구: {len(self.districts):,}개")
        print(f"탐지 시도: {len(self.provinces):,}개")
        print(f"회사: {len(self.companies):,}개")
    
    def _generate_detection_names(self):
        """탐지용 이름 생성 (모든 성씨 + 이름 조합)"""
        all_surnames = SINGLE_SURNAMES + COMPOUND_SURNAMES
        
        for surname in all_surnames:
            for first_name in KOREAN_FIRST_NAMES:
                full_name = surname + first_name
                if 2 <= len(full_name) <= 4:
                    if full_name not in NAME_EXCLUDE_WORDS and first_name not in NAME_EXCLUDE_WORDS:
                        self.real_names.add(full_name)
    
    def _prepare_address_data(self):
        """탐지용 주소 데이터 준비"""
        # 시/도
        for province in MAJOR_ADDRESSES['provinces']:
            short_name = province.replace('특별시', '').replace('광역시', '').replace('특별자치시', '').replace('도', '').replace('특별자치', '')
            self.provinces.add(short_name)
            self.real_addresses.add(short_name)
        
        # 시/군/구
        for district in MAJOR_ADDRESSES['districts']:
            self.districts.add(district)
            self.real_addresses.add(district)
        
        # 도로명
        for road in MAJOR_ADDRESSES['roads']:
            self.road_names.add(road)
            self.real_addresses.add(road)
        
        # 조합 주소 생성
        for province in ['서울', '부산', '대구', '인천', '광주', '대전']:
            for district in random.sample(MAJOR_ADDRESSES['districts'][:20], 5):
                self.real_addresses.add(f"{province} {district}")
    
    def _generate_company_data(self):
        """회사 데이터 생성"""
        company_types = ['전자', '기술', '시스템', '솔루션', '서비스', '컴퍼니', '그룹', '코퍼레이션']
        company_prefixes = ['한국', '동아', '대한', '신한', '우리', '하나', '국민', '삼성', 'LG', 'SK']
        
        for prefix in company_prefixes:
            for type_name in company_types:
                self.companies.append(f"{prefix}{type_name}")
    
    def _apply_custom_data(self, custom_data: Dict):
        """커스텀 데이터 적용"""
        if 'names' in custom_data:
            self.real_names.update(custom_data['names'])
        if 'addresses' in custom_data:
            self.real_addresses.update(custom_data['addresses'])
        if 'companies' in custom_data:
            self.companies.extend(custom_data['companies'])

# 전역 인스턴스
_global_pools = None

def get_pools() -> DataPools:
    """데이터풀 싱글톤 인스턴스"""
    global _global_pools
    if _global_pools is None:
        _global_pools = DataPools()
        _global_pools.initialize()
    return _global_pools

def initialize_pools(custom_data: Dict = None):
    """데이터풀 초기화"""
    global _global_pools
    _global_pools = DataPools()
    _global_pools.initialize(custom_data)

def reload_pools():
    """데이터풀 재로드"""
    global _global_pools
    _global_pools = None
    get_pools()

def get_pool_stats() -> Dict[str, int]:
    """데이터풀 통계"""
    pools = get_pools()
    return {
        "detection_names": len(pools.real_names),
        "detection_addresses": len(pools.real_addresses),
        "detection_roads": len(pools.road_names),
        "detection_districts": len(pools.districts),
        "detection_provinces": len(pools.provinces),
        "companies": len(pools.companies)
    }